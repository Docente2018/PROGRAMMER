private void burbujaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_burbujaActionPerformed
// TODO add your handling code here:
    Ordenamientos datos = new Ordenamientos(Integer.parseInt(CanNum.getText()));
    int[] valores;
    int i;
    valores = datos.getNumeros();

    DefaultListModel l1 = new DefaultListModel();
    for (i = 0; i < valores.length; ++i) {
        l1.addElement(valores[i]);
    }
    lista1.setModel(l1);

    valores = datos.Burbuja();

    DefaultListModel l2 = new DefaultListModel();
    for (i = 0; i < valores.length; ++i) {
        l2.addElement(valores[i]);
    }
    lista2.setModel(l2);
}//GEN-LAST:event_burbujaActionPerformed

